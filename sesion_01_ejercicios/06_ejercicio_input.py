'''
Ingresar 2 numeros por teclado, sumarlos e imprimir el resultado
'''

# Ingreso de datos
dato1 = input("Ingresar el primer numero : ")
dato2 = input("Ingresar el segundo numero : ")

# Tratamientos de datos

# Funcion int() convierte un dato a entero, si es posible
dato1 = int(dato1)
dato2 = int(dato2)


# Procesamiento de los datos
resultado = dato1 + dato2

# Resultado
print("El resultado de la suma es :", resultado)



